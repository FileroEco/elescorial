// Reverse lookup: given a path like "/sobre-mi/", find the equivalent for another language
export function getPathForLang(currentPath, currentLang, targetLang) {
  // Strip leading/trailing slashes for comparison
  const cleanPath = currentPath.replace(/^\/|\/$/g, '');
  
  // Search SLUG_MAP for a match
  for (const [key, map] of Object.entries(SLUG_MAP)) {
    if (map[currentLang] === cleanPath) {
      return `/${targetLang}/${map[targetLang]}/`;
    }
  }
  
  // No mapping found = path is the same in all languages
  return `/${targetLang}/${cleanPath}/`;
}

// Helper: generar hreflang links para una ruta
// pathOrKey puede ser una ruta normal ('/el-escorial/') o una key del SLUG_MAP ('about')
export function getHreflangLinks(pathOrKey) {
  const map = SLUG_MAP[pathOrKey];
  if (map) {
    return SITE.languages.map(lang => ({
      lang,
      href: `${SITE.domain}/${lang}/${map[lang]}/`,
    })).concat([{
      lang: 'x-default',
      href: `${SITE.domain}/en/${map['en']}/`,
    }]);
  }
  return SITE.languages.map(lang => ({
    lang,
    href: `${SITE.domain}/${lang}${pathOrKey}`,
  })).concat([{
    lang: 'x-default',
    href: `${SITE.domain}/en${pathOrKey}`,
  }]);
}

// Helper: obtener la ruta correcta en otro idioma para el language switcher
export function getLangSwitchPath(pathOrKey, targetLang) {
  const map = SLUG_MAP[pathOrKey];
  if (map) {
    return `/${targetLang}/${map[targetLang]}/`;
  }
  return `/${targetLang}${pathOrKey}`;
}
